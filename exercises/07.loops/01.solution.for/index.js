// Numbers 1 through 5
console.log('Numbers 1-5:')
for (let i = 1; i <= 5; i++) {
	console.log(i)
}

// Even numbers from 2 to 10
console.log('Even numbers 2-10:')
for (let i = 2; i <= 10; i += 2) {
	console.log(i)
}

// Countdown from 5 to 1
console.log('Countdown:')
for (let i = 5; i >= 1; i--) {
	console.log(i)
}
